def tr(lang: str, key: str) -> str:
    lang = lang if lang in SUPPORTED_LANGS else DEFAULT_LANG
    return I18N.get(lang, I18N[DEFAULT_LANG]).get(key, I18N[DEFAULT_LANG].get(key, key))


def detect_lang(headers, params: dict[str, list[str]]) -> str:
    # 1. Explicit URL override: ?lang=pl/en/de/cs/sk
    lang = (params.get("lang") or [""])[0].strip().lower()
    if lang in SUPPORTED_LANGS:
        return lang

    # 2. Cookie override set by the language switcher
    cookie = headers.get("Cookie", "") if headers else ""
    m = re.search(r"(?:^|;\s*)" + re.escape(LANG_COOKIE) + r"=([a-z]{2})", cookie)
    if m and m.group(1) in SUPPORTED_LANGS:
        return m.group(1)

    # 3. Browser / HA Ingress language
    header = headers.get("Accept-Language", "") if headers else ""
    for part in header.split(","):
        code = part.split(";")[0].strip().lower()
        base = code.split("-")[0]
        if base in SUPPORTED_LANGS:
            return base

    return DEFAULT_LANG


def localize_html(html_text: str, lang: str) -> str:
    lang = lang if lang in SUPPORTED_LANGS else DEFAULT_LANG
    # Longest variants first so e.g. "No key (recommended)" wins before "No key".
    replacements: list[tuple[str, str]] = []
    for key in LOCALIZE_EXACT_KEYS:
        target = tr(lang, key)
        for source in SOURCE_TEXT.get(key, []):
            if source != target:
                replacements.append((source, target))
    for source, target in sorted(replacements, key=lambda x: len(x[0]), reverse=True):
        html_text = html_text.replace(source, target)
    return html_text


def lang_switcher(active_lang: str) -> str:
    links = []
    for code in ["en", "de", "cs", "sk", "pl"]:
        cls = "active" if code == active_lang else ""
        links.append(f'<a class="{cls}" href="?lang={code}" onclick="setLang(\'{code}\');return false;">{code.upper()}</a>')
    return '<div class="lang-switch">' + ''.join(links) + '</div>'


